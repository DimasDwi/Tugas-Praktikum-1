public class Enam {
    public static void main(String[] args) {

        int first = 1;
        int last = 10;
        int jumlah = 0;
        while (first <= last){
            jumlah = jumlah + first;
            first++;
        }

        System.out.println("Hasilnya adalah...." + jumlah);
    }
}
